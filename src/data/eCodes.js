// E-kod kontrolü fonksiyonu
export const checkECode = (code) => {
  const normalized = code.toUpperCase().replace(/\s/g, '');
  return eCodeDatabase[normalized] || null;
};

// Birden fazla E-kod kontrolü
export const checkMultipleECodes = (codes) => {
  return codes.map(code => ({
    code,
    ...checkECode(code)
  })).filter(item => item.name);
};

// Haram/şüpheli kod var mı?
export const hasHaramOrSuspicious = (codes) => {
  for (const code of codes) {
    const info = checkECode(code);
    if (info && (info.status === 'haram' || info.status === 'suspicious')) {
      return { found: true, code, info };
    }
  }
  return { found: false };
};
