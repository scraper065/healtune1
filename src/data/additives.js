// Additive'i getir
export const getAdditive = (code) => {
  const cleanCode = code.toUpperCase().trim();
  return ADDITIVES_DB[cleanCode] || {
    name: code,
    type: 'unknown',
    risk: 'unknown',
    halal: 'bilinmiyor'
  };
};

// Risk seviyesini getir
export const getRiskLevel = (riskScore) => {
  if (riskScore <= 30) return 'low';
  if (riskScore <= 65) return 'medium';
  return 'high';
};

// Risk skorunu hesapla (0-100)
export const calculateRiskScore = (additive) => {
  const baseScore = {
    low: 20,
    medium: 60,
    high: 90
  };
  return baseScore[additive.risk] || 50;
};
