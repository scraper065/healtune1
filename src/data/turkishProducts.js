// Marka kontrolü
export const isTurkishBrand = (brand) => {
  if (!brand) return false;
  const lower = brand.toLowerCase();
  return turkishBrandsList.some(b => lower.includes(b));
};

export const isBoycottBrand = (brand) => {
  if (!brand) return false;
  const lower = brand.toLowerCase();
  return boycottBrandsList.some(b => lower.includes(b));
};
